def get_action_dialogue(personality, action_type, archetype=None, name=None, hand_strength=None):
    """根据动作类型获取对话"""
    key = ACTION_TO_KEY.get(action_type)
    if not key:
        return None
    return get_dialogue(personality, key, archetype, name, hand_strength)


def get_thinking_time(personality, archetype=None, hand_strength=None, is_all_in_situation=False):
    """根据性格和局势计算思考时间（秒）

    Returns:
        float: 建议思考时间
    """
    category = _get_category(personality, archetype)

    # 基础思考时间（秒）
    base_times = {
        "aggressive": 0.9,
        "tight": 1.8,
        "caller": 1.2,
        "shark": 1.5,
        "beginner": 2.0,
    }
    base = base_times.get(category, 1.2)

    # 手牌强度影响：强牌和难决策牌会想更久
    if hand_strength is not None:
        if hand_strength > 0.8:
            base += 0.5  # 强牌要思考如何价值最大化
        elif hand_strength < 0.25:
            base += 0.5  # 弱牌/诈唬要思考怎么演
        elif 0.4 <= hand_strength <= 0.6:
            base += 0.4  # 边缘牌最难决策

    # 全押局面更紧张，思考更久
    if is_all_in_situation:
        base += 1.0

    # 加入一点随机性，避免机械化
    jitter = random.uniform(-0.15, 0.35)
    return max(0.8, base + jitter)
